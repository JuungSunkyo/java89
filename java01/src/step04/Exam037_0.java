//주제 : 연산자 - '+'연산자의 특별한 용법
package step04;

public class Exam037_0 {
  public static void main(String[] args) {

    int a = 10;
    a = a + 1;
    System.out.println(a);

    int b = 10;
    ++b;
    System.out.println(a);

    int c = 10;
    System.out.println(++c);

    a = 10;
    --a;
    System.out.println(a);

    b = 10;
    System.out.println(--b);
  }

}
