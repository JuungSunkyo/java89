//주제 :  Literal-정수의 다양한 진수 표기법
package step02;

public class Exam023 {
  public static void main(String[] args) {
    System.out.println(100); //10
    System.out.println(0144);//8
    System.out.println(0x64);
    System.out.println(0X64); //16
    System.out.println(0b01100100); //2
    System.out.println(0B01100100);
  }
}
