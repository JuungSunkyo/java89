//주제 :  변수 선언 - 기본 타입을 묶어 새로운 타입을 정의(II)
package step03;

public class Exam036 {
  public static void main(String[] args) {
    int a = 10, b = 7;
    int result = a + b;
    System.out.println(result);

    result = a - b;
    System.out.println(result);

    result = a * b;
    System.out.println(result);

    result = a / b;
    System.out.println(result);

    result = a % b;
    System.out.println(result);



  }
}
